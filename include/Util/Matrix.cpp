void	clFloatMatrix2::NormData()
{
	int dim = Size();
	float sum = 0;
	for (int i = 0; i < dim; i++)
		sum += _arr[i];
	if (sum > DOUBLE_0)
		for (int i = 0; i < dim; i++)
			_arr[i] /= sum;
}
